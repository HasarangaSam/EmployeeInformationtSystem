    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try {
        String id = jTextField1.getText();
        pst = con.prepareStatement("SELECT * FROM records WHERE id=?");
        pst.setString(1, id);
        ResultSet rs = pst.executeQuery();
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear table
        
        if (rs.next()) {
            Vector row = new Vector();
            row.add(rs.getString("id"));
            row.add(rs.getString("fname"));
            row.add(rs.getString("lname"));
            row.add(rs.getString("city"));
            row.add(rs.getString("phone"));
            row.add(rs.getString("salary"));
            model.addRow(row);
            jTextField1.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "No record found for ID: " + id, "Search Result", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.setText("");
        }
    } catch (SQLException ex) {
        Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_searchButtonActionPerformed


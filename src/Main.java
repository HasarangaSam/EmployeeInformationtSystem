    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
         try {
        String fname, lname, city, phone, salary;
        fname = fnameField.getText();
        lname = lnameField.getText();
        city = cityField.getText();
        phone = phoneField.getText();
        salary = salaryField.getText();

        // Check if any field is empty
        if (fname.isEmpty() || lname.isEmpty() || city.isEmpty() || phone.isEmpty() || salary.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Check if phone is numeric
        if (!phone.matches("\\d+") || !salary.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Phone number and Salary must be numeric", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        pst = con.prepareStatement("INSERT INTO records(fname,lname,city,phone,salary) VALUES(?,?,?,?,?)");
        
        pst.setString(1, fname);
        pst.setString(2, lname);
        pst.setString(3, city);
        pst.setString(4, phone);
        pst.setString(5, salary);
        
        pst.executeUpdate();
        
        JOptionPane.showMessageDialog(this, "Record Added");
        tableUpdate();
        
        fnameField.setText("");
        lnameField.setText("");
        cityField.setText("");
        phoneField.setText("");
        salaryField.setText("");
        fnameField.requestFocus();
                   
    } catch (SQLException ex) {
        Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
     }      
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        
        if(selectedIndex != -1){
            int id = Integer.parseInt(model.getValueAt(selectedIndex, 0).toString());
        
        try {
            // Prepare SQL update statement
            pst = con.prepareStatement("DELETE FROM records WHERE id=?");
            
            pst.setInt(1, id);
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Record Deleted");
            tableUpdate();
            
            fnameField.setText("");
            lnameField.setText("");
            cityField.setText("");
            phoneField.setText("");
            salaryField.setText("");
            fnameField.requestFocus();
                       
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        } else{
            JOptionPane.showMessageDialog(this,"Please select a row","Alert",JOptionPane.WARNING_MESSAGE);
        }       
    }//GEN-LAST:event_deleteButtonActionPerformed

